		if k == id then
			return v
		end
	end
	return {100,1,{1,2,3,4}}
end

function isCorrectFuel(vehid,id)
	local fuels = getVehicleFuelInfo(vehid)[3]
	for k,v in pairs(fuels) do
		if v == id then
			return true
		end
	end
	return false
end